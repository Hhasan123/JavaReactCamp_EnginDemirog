package Ders3;

public class User {
	private int id;
	private String e_Mail;
	private String password;
	private String photoBiseyisi;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getE_Mail() {
		return e_Mail;
	}
	public void setE_Mail(String e_Mail) {
		this.e_Mail = e_Mail;
	}
	
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	public String getPhotoBiseyisi() {
		return photoBiseyisi;
	}
	public void setPhotoBiseyisi(String photoBiseyisi) {
		this.photoBiseyisi = photoBiseyisi;
	}
}
